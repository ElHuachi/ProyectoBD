        public void actualizarTabla() {
        try {

            DefaultTableModel modelo = (DefaultTableModel) tblC.getModel();
            ArrayList<Categoria> listaProductos = new DAOCategoria().consultarTodos();
            modelo.setRowCount(0);
            for (Categoria c : listaProductos) {
                Object[] fila = new Object[]{
                    c.getIdCategoria(),
                    c.getCategoria(),
                };

                modelo.addRow(fila);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
